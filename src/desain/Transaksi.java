    public void tampil(){
        Object [] baris = {"NO","ID Transaksi", "ID Sepeda", "ID Pelanggan", "Tanggal Sewa" ,"Lama Sewa", "Total Harga"};
        tabMode = new DefaultTableModel(null, baris);
        tableTransaksi.setModel(tabMode);
        try {
            String sql = "SELECT * FROM transaksi";
            st = conn.createStatement();
            rs = st.executeQuery(sql);
            int no = 0;
            while (rs.next()){
                    no++;
                    
                    
                    String id_transaksi = rs.getString("id_transaksi");
                    String id_sepeda = rs.getString("id_sepeda");
                    String id_pelanggan = rs.getString("id_pelanggan");
                    String tanggal = rs.getString("tanggal_sewa");
                    String lama = rs.getString("lama_sewa");
                    String total = rs.getString("total_harga");

                    Object [] data = {no,id_transaksi,id_sepeda,id_pelanggan,tanggal,lama,total};
                    tabMode.addRow(data);
            }
        } catch (Exception e){
                System.out.println(e.toString());
        }
    }
    
