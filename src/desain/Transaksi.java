    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String id_sepeda = cmbIDSepeda.getSelectedItem().toString();
        String id_pelanggan = cmbIDPelanggan.getSelectedItem().toString();
        
        String tgl = cmbTanggal.getSelectedItem().toString();
        String bln = cmbBulan.getSelectedItem().toString();
        String thn = cmbTahun.getSelectedItem().toString();
        String tanggal_sewa = thn+"-"+bln+"-"+tgl;

        try {
            int s;
            String sql = "INSERT INTO transaksi VALUES('"+txtIDTransaksi.getText()+"','"+id_sepeda+"','"+id_pelanggan+"'  , '"+tanggal_sewa+"'  , '"+txtLama.getText()+"', '"+txtTotal.getText()+"')";
            st = conn.createStatement();
            s = st.executeUpdate(sql);
            if (s == 1){
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                clear();
                tampil();
            }
        } catch (Exception e){
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

